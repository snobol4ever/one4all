#ifndef TERM_H
#define TERM_H
#include <stddef.h>
typedef struct Term Term;
typedef enum {
    TERM_ATOM,
    TERM_VAR,
    TERM_COMPOUND,
    TERM_INT,
    TERM_FLOAT,
    TERM_REF
} TermTag;
struct Term {
    TermTag tag;
    int     saved_slot;
    union {
        int atom_id;
        int var_slot;
        struct {
            int    functor;
            int    arity;
            Term **args;      /* heap array of arity Term* pointers   */
        } compound;
        long ival;
        double fval;
        Term *ref;
    };
};
/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
static inline Term *term_deref(Term *t) {
    while (t && t->tag == TERM_REF) t = t->ref;
    return t;
}
/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
Term *term_new_atom(int atom_id);
/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
Term *term_new_var(int var_slot);
/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
Term *term_new_compound(int functor, int arity, Term **args);
/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
Term *term_new_int(long ival);
/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
Term *term_new_float(double fval);
/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
static inline Term *term_new_unbound(int slot) {
    return term_new_var(slot);
}
extern int ATOM_DOT;
extern int ATOM_NIL;
extern int ATOM_TRUE;
extern int ATOM_FAIL;
extern int ATOM_CUT;
#endif
