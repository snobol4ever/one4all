static int emit_expression_registry(FILE *out, const SM_sequence_t *prog)
{
    int n = 0;
    for (int i = 0; i < prog->count; i++) {
        const SM_t *ins = &prog->instrs[i];
        if (ins->op == SM_LABEL && ins->a[0].s && *ins->a[0].s && ins->a[2].i == 1)
            n++;
    }
    if (n == 0) return 0;
    if (emit_three_column_line(out, "", ".section", ".data", NULL) != 0) return -1;
    if (emit_three_column_line(out, "", ".align",   "8",     NULL) != 0) return -1;
    if (emit_three_column_line(out, ".Lexpression_registry:", "", "", NULL) != 0) return -1;
    for (int i = 0; i < prog->count; i++) {
        const SM_t *ins = &prog->instrs[i];
        if (ins->op != SM_LABEL || !ins->a[0].s || !*ins->a[0].s || ins->a[2].i != 1) continue;
        int str_idx = strtab_lookup(ins->a[0].s);
        if (str_idx < 0) continue;
        int entry_pc = i + 1;
        char name_arg[32], fn_arg[32];
        snprintf(name_arg, sizeof(name_arg), ".S%d", str_idx);
        snprintf(fn_arg,   sizeof(fn_arg),   ".L%d", entry_pc);
        if (emit_three_column_line(out, "", ".quad", name_arg, NULL) != 0) return -1;
        if (emit_three_column_line(out, "", ".quad", fn_arg,   NULL) != 0) return -1;
    }
    if (emit_three_column_line(out, "", ".quad", "0", NULL) != 0) return -1;
    if (emit_three_column_line(out, "", ".quad", "0", NULL) != 0) return -1;
    if (emit_three_column_line(out, "", ".text", "",  NULL)        != 0) return -1;
    return n;
}
