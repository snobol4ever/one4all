/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
void emit_sm_op(int op)
{
    for (const sm_nullary_entry_t *e = g_sm_nullary; e->op >= 0; e++)
        if (e->op == op) { emit_sm_nullary_rt(e->macro_name, e->rt_fn); return; }
    emit_sm_unhandled_op(op);
}
