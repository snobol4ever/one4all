static void strtab_collect(const SM_sequence_t *prog)
{
    strtab_reset();
    for (int i = 0; i < prog->count; i++) {
        const SM_t *ins = &prog->instrs[i];
        switch (ins->op) {
        case SM_PUSH_LIT_S:
        case SM_PUSH_LIT_CS:
        case SM_PUSH_VAR:
        case SM_STORE_VAR:
        case SM_PAT_LIT:
        case SM_PAT_REFNAME:
        case SM_PAT_CAPTURE:
        case SM_PAT_CAPTURE_FN:
        case SM_PAT_USERCALL:
        case SM_PAT_CAPTURE_FN_ARGS:
        case SM_PAT_USERCALL_ARGS:
        case SM_EXEC_STMT:
        case SM_CALL_FN:
        case SM_LABEL:
            if (ins->a[0].s) strtab_intern(ins->a[0].s);
            break;
        default:
            break;
        }
        switch (ins->op) {
        case SM_PAT_CAPTURE_FN:
        case SM_PAT_USERCALL:
            if (ins->a[2].s) strtab_intern(ins->a[2].s);
            break;
        default:
            break;
        }
    }
}
