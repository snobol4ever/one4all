/* Emit the per-program predicate registry. Returns count of entries emitted (0 if no Prolog predicates).     */
/* Names + sval strings have already been interned by pl_pre_intern_pred_names() so strtab_label works.       */
static int emit_pl_predicate_registry(FILE *out)
{
    int n = 0;
    for (int i = 0; i < g_pl_bb_count; i++) {
        if (g_pl_bb_table[i].name && bb_graph_of_pred(&g_pl_bb_table[i])) n++;
    }
    if (n == 0) return 0;
    for (int i = 0; i < g_pl_bb_count; i++) {
        const Pl_PredEntry_BB *e = &g_pl_bb_table[i];
        if (!e->name || !bb_graph_of_pred(e)) continue;
        if (emit_pl_builder_fn(out, i, e) != 0) return -1;
    }
    if (emit_three_column_line(out, "", ".section", ".data", NULL) != 0) return -1;
    if (emit_three_column_line(out, "", ".align",   "8",     NULL) != 0) return -1;
    if (emit_three_column_line(out, ".Lpl_registry:", "", "", NULL) != 0) return -1;
    for (int i = 0; i < g_pl_bb_count; i++) {
        const Pl_PredEntry_BB *e = &g_pl_bb_table[i];
        if (!e->name || !bb_graph_of_pred(e)) continue;
        char name_arg[64], arity_arg[32], fn_arg[64];
        strtab_label(name_arg, sizeof(name_arg), e->name);
        snprintf(arity_arg, sizeof(arity_arg), "%d", e->arity);
        snprintf(fn_arg, sizeof(fn_arg), ".Lpl_builder_%d", i);
        if (emit_three_column_line(out, "", ".quad", name_arg, NULL) != 0) return -1;
        if (emit_three_column_line(out, "", ".int",  arity_arg, NULL) != 0) return -1;
        if (emit_three_column_line(out, "", ".int",  "0", NULL) != 0) return -1;
        if (emit_three_column_line(out, "", ".quad", fn_arg, NULL) != 0) return -1;
    }
    if (emit_three_column_line(out, "", ".quad", "0", NULL) != 0) return -1;
    if (emit_three_column_line(out, "", ".int",  "0", NULL) != 0) return -1;
    if (emit_three_column_line(out, "", ".int",  "0", NULL) != 0) return -1;
    if (emit_three_column_line(out, "", ".quad", "0", NULL) != 0) return -1;
    if (emit_three_column_line(out, "", ".text", "", NULL) != 0) return -1;
    return n;
}
#define MAX_CAP_FIXUPS 1024
typedef struct {
    void       *cap_ptr;
    char        child_label[128];
    char        data_label[128];
    char        varname[128];
    int         is_arbno;
    int         is_callcap;
    int         immediate;
} cap_fixup_t;
static cap_fixup_t g_cap_fixups[MAX_CAP_FIXUPS];
static int         g_cap_fixups_n = 0;
/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
static void cap_fixups_reset(void) { g_cap_fixups_n = 0; }
/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
static void cap_fixup_add(void *cap_ptr, const char *child_label)
{
    if (g_cap_fixups_n >= MAX_CAP_FIXUPS) return;
    cap_fixup_t *fx = &g_cap_fixups[g_cap_fixups_n];
    fx->cap_ptr      = cap_ptr;
    fx->data_label[0] = '\0';
    fx->varname[0]    = '\0';
    fx->is_arbno      = ((uintptr_t)cap_ptr == 2);
    fx->is_callcap    = 0;
    fx->immediate     = 0;
    const char *pipe = child_label ? strchr(child_label, '|') : NULL;
    if (pipe) {
        int dlen = (int)(pipe - child_label);
        if (dlen >= 128) dlen = 127;
        memcpy(fx->data_label, child_label, dlen);
        fx->data_label[dlen] = '\0';
        const char *rest = pipe + 1;
        const char *pipe2 = strchr(rest, '|');
        if (pipe2) {
            int clen = (int)(pipe2 - rest);
            if (clen >= 128) clen = 127;
            memcpy(fx->child_label, rest, clen);
            fx->child_label[clen] = '\0';
            rest = pipe2 + 1;
            const char *pipe3 = strchr(rest, '|');
            if (pipe3) {
                int vlen = (int)(pipe3 - rest);
                if (vlen >= 128) vlen = 127;
                memcpy(fx->varname, rest, vlen);
                fx->varname[vlen] = '\0';
                rest = pipe3 + 1;
                const char *pipe4 = strchr(rest, '|');
                if (pipe4) {
                    fx->immediate  = atoi(rest);
                    fx->is_callcap = atoi(pipe4 + 1);
                } else {
                    fx->immediate = atoi(rest);
                }
            } else {
                snprintf(fx->varname, sizeof(fx->varname), "%s", rest);
            }
        } else {
            snprintf(fx->child_label, sizeof(fx->child_label), "%s", rest);
        }
    } else {
        snprintf(fx->child_label, sizeof(fx->child_label), "%s", child_label ? child_label : "");
    }
    g_cap_fixups_n++;
}
