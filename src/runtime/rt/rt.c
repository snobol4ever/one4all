/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
void rt_halt_tos(void)
{
    int rc = 0;
    int depth = g_ops->depth();
    if (depth > 0) {
        DESCR_t d = vstack_peek();
        if (d.v == DT_I) { rc = (int)d.i; g_ops->set_depth(depth - 1); }
    }
    g_halt_rc  = rc;
    g_halt_set = 1;
}
