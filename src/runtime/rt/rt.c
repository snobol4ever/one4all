/*----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
DESCR_t rt_bb_cap(void *zeta, int port)
{
    cap_t *ζ;
    void *slot0 = *(void **)zeta;
    if (slot0 && !bb_in_pool(slot0) && ((cap_t *)slot0)->fn) ζ = (cap_t *)slot0;
    else ζ = (cap_t *)zeta;
    DESCR_t cr;
    int pre_δ;
    if (port == 0) {
        ζ->has_pending = 0;
        if (!ζ->immediate) rt_register_cap(ζ);
        pre_δ = Δ;
        ζ->cap_start = Δ;
        cr = ζ->fn(ζ->state, 0);
        if (IS_FAIL_fn(cr)) goto cap_fail;
        cr = descr_match_span(Σ + pre_δ, Δ - pre_δ);
        goto cap_commit;
    }
    if (!ζ->immediate && ζ->has_pending) { NAME_pop(); ζ->has_pending = 0; }
    cr = ζ->fn(ζ->state, 1);
    if (IS_FAIL_fn(cr)) goto cap_fail;
    cr = descr_match_span(Σ + ζ->cap_start, Δ - ζ->cap_start);
cap_commit:
    if (cr.v != DT_S) cr = descr_match_span(Σ + Δ, 0);
    if (ζ->immediate) {
        char *s = (char *)GC_MALLOC(cr.slen + 1);
        if (cr.s && cr.slen > 0) memcpy(s, cr.s, (size_t)cr.slen);
        s[cr.slen] = '\0';
        DESCR_t val = { .v = DT_S, .slen = cr.slen, .s = s };
        if (name_commit_value(&ζ->name, val) < 0) goto cap_fail;
    } else {
        (void) NAME_push(&ζ->name, cr.s, (int)cr.slen);
        ζ->pending     = cr;
        ζ->has_pending = 1;
    }
    return cr;
cap_fail:
    if (!ζ->immediate && ζ->has_pending) { NAME_pop(); ζ->has_pending = 0; }
    return FAILDESCR;
}
#define RT_MAX_CAPTURES 256
